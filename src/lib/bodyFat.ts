export function getBFFromTable(sum: number, gender?: string | null): number | null {
  if (sum < 15) return null;
  
  // Default to Men if gender is unknown or not explicitly "Perempuan" or "P"
  const isFemale = gender === 'Perempuan' || gender === 'P';
  const chart = isFemale ? bodyFatChartWomen : bodyFatChartMen;
  
  if (chart[sum] !== undefined) return chart[sum];
  
  const keys = Object.keys(chart).map(Number).sort((a, b) => a - b);
  let lowerBound = keys[0];
  for (const key of keys) {
    if (key <= sum) lowerBound = key;
    else break;
  }
  return chart[lowerBound];
}
